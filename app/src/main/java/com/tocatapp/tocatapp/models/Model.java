package com.tocatapp.tocatapp.models;

public abstract class Model {

    protected int id;
    protected int name;

    public Model() {};

    public Model(int id, int name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getName() {
        return name;
    }

    public void setName(int name) {
        this.name = name;
    }
}
